       <p>Have you ever been a part of a fundraiser? Have you ever had to sell raffles for a good cause, your sports team, your school? If you have, you’ll probably agree with me that it sucks. It’s one of the most stressful things a person can do. It’s like trying to pull the teeth out of every single person you know without any anesthetics, there’s always screaming, yelling, squirming, and hard feelings. And just think of all the people who do that kind of stuff for a living. How do they do it? I’m pretty sure I would be bald by now if I had to do it for a living from pulling my hair out wondering “Why is it so hard for people to give to a good cause?” It usually includes trying to guilt trip people into giving or giving them something in return for their money, in which it becomes a business. <a href="http://cornellcru.com/2010/10/pulling-out-teeth/#more-629" class="more-link">[...more]</a></p> 
